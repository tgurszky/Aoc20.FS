let normalize (length: double) (y: double) =
    if y < length then y else
    y - floor(y / length) * length

let fromRow y (row: char list) =
    let ny = normalize (double row.Length) y
    List.item (int ny) row

let trim (s: string) =
    s.Trim()

let isTree (testPattern: string) x y =
    testPattern.Split('\n')
    |> Array.toList
    |> List.item y
    |> trim
    |> Seq.toList
    |> fromRow x
    |> (=) '#'

let height (testPattern: string) =
    testPattern.Split('\n')
    |> Array.length

let solve dx dy testPattern =
    let treeValue x y testPattern =
        if isTree testPattern x y then 1 else 0
    let rec countTrees x y testPattern =
        if (y + 1) > height testPattern then 0
        else (treeValue x y testPattern) + (countTrees (x + dx) (y + dy) testPattern)
    countTrees 0. 0 testPattern

let multiplySlopes slopes testPattern =
    let calc (dx, dy) =
        solve dx dy testPattern
    List.map calc slopes
    |> List.reduce (*)

